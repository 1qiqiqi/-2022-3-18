package _位运算左右移动;

public class _移动计数技巧 {
    public static void main(String[] args) {
        int n = 6;
        System.out.println("左移动：操作数*2^操作数");
        System.out.println(6<<2);
        System.out.println(4<<2);
        System.out.println("右移动：操作数/2^操作数");
        System.out.println(6>>2);
        System.out.println(4>>2);

    }
}
